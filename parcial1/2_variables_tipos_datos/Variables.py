# Las variables son debilmente tipadas
# es decir no es necesario indicar el tipo de
# dato al momento de utilizarlas

# Variables
edad = 30
estatura = 1.80
correo_electronico = "dagfiscal@gmail.com"
logica = True
caracter = '@'

# Imprimir contenido de variables
print(edad)
print(estatura)
print(correo_electronico)
print(logica)
print(caracter)

# Conocer el tipo de dato que Python asigno
print(type(edad))
print(type(estatura))
print(type(correo_electronico))
print(type(logica))
print(type(caracter))